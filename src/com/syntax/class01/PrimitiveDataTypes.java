package com.syntax.class01;

public class PrimitiveDataTypes {
	
	public static void main(String[] args) {
		//to store whole numbers
		byte variableOne=10;
		short variableTwo=100;
		int variableTree=1000;
		long longValue=1000000;
		//to store single character
		char singleValue='F';
		char otherValue='@';
		//to store decimal numbers
		float variableFive=12.96f;
		double anyName=132.45678;
		//true or false
		boolean thirsty=false;
		boolean hungry=true;
		System.out.println(thirsty);
		
		
		// create a variable and store value
		//declaring a variable and assigning
		boolean b=true;
		//1 step -> declare variable
		//2 step -> assign value
		int i;
		i=1000;
		
		
		char x,y,z;
		x='A';
		y='&';
		z='0';
		//we can't have double i=12.99; java will complain
		
		z='#'; //reassigning the value
		System.out.println(z);
		
	}

}
